# operator "=" is used to assign the literal to a identifier as shown below
a = 5
b = 10
c= a+b
print(c)

# Operator "==" is used to compare the two literals
# since both a and b values are not equal hence it will return the boolean result as false
print(a==b)
x = 6
y = 6
# it print true as value of x and y are equal
print (x==y)

# Operator ** is used to calculate the power of the number
s = 5
print (s**2)

# Operator ^ is used to calculate the xor of the two literals
# converts the decimals to binary and then Xor them
m = 5 #0101
n = 6 #0110
print (m^n) #0011
